#1. when an wanted input is inputted by user for both the numerator and denominator
#2. will occur when the user enters 0 for the denominator
#3. message before users inputs, while loop for while input for denominator > or < 0

try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
except ZeroDivisionError:
    print("Cannot divide by zero!")
